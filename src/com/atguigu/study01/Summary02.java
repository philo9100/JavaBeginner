/*
 * 对第一个java程序进行总结
 *
 * 1. java程序编写-编译-运行的过程
 * 编写:我们将编写的java代码保存在以".java"结尾的源文件中
 * 编译:我们使用javac.exe命令编译我们的java源文件，格式：javac 源文件名.java
 *     （路径指向源文件所在的路径）
 * 运行:使用java.exe命令解释运行我们的字节码文件，格式：java 类名
 *
 * 2.
 * 在一个java源文件中可以声明多个class。但是，最多只能有一个类声明为public的
 * 而且要求声明为public的类的类名必须与源文件名相同
 * 其实在创建完 源文件名.java 的java类型文件后就会自动生成一个 public 源文件名 的类
 *
 * 3.
 * 程序的入口是main()方法，格式是固定的
 * public static void main(String[] args){ //args=arguments: 参数 }
 * //public static void main(String a[]){}
 *
 * 4.输出语句:
 * System.out.println(""); //先输出数据 ，再换行
 * System.out.print(""); // 只输出数据
 * System.out.println(); //可以起到换行的作用
 *
 * 5.每一行执行语句都以" ; "结束
 *   要么是以" } " 结束
 *
 * 6.编译的过程:编译以后，会生成一个或多个字节码文件（取决于类的个数）。
 *             字节码文件的文件名与java源文件中的类名相同
 *
 * */
